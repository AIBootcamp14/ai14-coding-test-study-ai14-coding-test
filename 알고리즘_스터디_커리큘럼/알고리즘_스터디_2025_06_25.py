def solution(str1, str2):
    if str1 not in str2:
        return 0
    else:
        return 1

print(solution("abc", "aabcc"))
print(solution("tbt", "tbbttb"))


# 간결화
def solution(str1, str2):
    return 0 if str1 not in str2 else 1

