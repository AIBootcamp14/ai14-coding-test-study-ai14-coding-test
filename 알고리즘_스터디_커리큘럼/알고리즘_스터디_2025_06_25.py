## 번외 문제 1 : [PCCE 기출문제] 10번 / 데이터 분석

# 기본 dict활용 풀이
def solution(data, ext, val_ext, sort_by):
    answer = []
    data_map = {
        "code" : 0,
        "date" : 1,
        "maximum" : 2,
        "remain" : 3
    }
    for i in data:
        if i[data_map[ext]] < val_ext:
            answer.append(i)
    answer.sort(key = lambda x: x[data_map[sort_by]])
    return answer

print(solution([[1, 20300104, 100, 80], [2, 20300804, 847, 37], [3, 20300401, 10, 8]], "date", 20300501, "remain"))


# dict + 리스트 컴프리헨션 풀이
def solution(data, ext, val_ext, sort_by):
    data_map = {
        "code" : 0,
        "date" : 1,
        "maximum" : 2,
        "remain" : 3
    }
    answer = [i for i in data if i[data_map[ext]] < val_ext]
    return sorted(answer, key = lambda x: x[data_map[sort_by]])

print(solution([[1, 20300104, 100, 80], [2, 20300804, 847, 37], [3, 20300401, 10, 8]], "date", 20300501, "remain"))