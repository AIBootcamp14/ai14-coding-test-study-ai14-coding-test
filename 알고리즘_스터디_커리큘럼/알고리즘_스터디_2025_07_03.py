## 문제 40 : 특별한 이차원 배열 1

# 기본 for문 풀이
def solution(n):
    answer = []
    for i in range(n):
        answer.append([0 for j in range(n)])
        answer[i][i] = 1
    return answer

print(solution(3))
print(solution(6))
print(solution(1))


