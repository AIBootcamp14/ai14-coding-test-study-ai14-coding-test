## 문제 41 : 가장 큰 수 찾기

# 기본 sorted 활용식
def solution(array):
    sorted_arr = sorted(array)
    answer = [sorted_arr[-1]]
    for i in range(len(array)):
        if array[i] == sorted_arr[-1]:
            answer.append(i)
    return answer

print(solution([1, 8, 3]))
print(solution([9, 10, 11, 8]))


# max 활용한 리스트 컴프리헨션
def solution(array):
    return [max(array), array.index(max(array))]

print(solution([1, 8, 3]))
print(solution([9, 10, 11, 8]))


