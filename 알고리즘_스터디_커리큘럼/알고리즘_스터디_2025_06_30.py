## 문제 31 : A 강조하기

def solution(myString):
    return ''.join([i.upper() if i == "a" or i == "A" else i.lower() for i in myString])

print(solution("abstract algebra"))
print(solution("PrOgRaMmErS"))
print(solution("AAAicecreaM"))


