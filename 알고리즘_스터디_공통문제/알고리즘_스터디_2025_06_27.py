# 번외 문제 2 : 카드 맞추기

# 기본 pop()을 사용한 풀이
def solution(cards1, cards2, goal):
    answer = []
    for i in range(len(goal)):
        if cards1 and goal[i] == cards1[0]:
            answer.append(cards1.pop(0))
        elif cards2 and goal[i] == cards2[0]:
            answer.append(cards2.pop(0))
    return "Yes" if answer == ["i", "want", "to", "drink", "water"] else "No"


print(solution(["i", "drink", "water"], ["want", "to"], ["i", "want", "to", "drink", "water"]))
print(solution(["i", "drink", "water"], ["want", "to"], ["want", "i", "to", "drink", "water"]))


# deque를 이용한 시간복잡도 우월한 풀이
# deque를 사용한 queue 방식은 포인터 지정 개념이라 FIFO 방식에 유리함
from collections import deque
def solution(cards1, cards2, goal):
    answer = []
    dq_cards1 = deque(cards1)
    dq_cards2 = deque(cards2)
    for i in range(len(goal)):
        if dq_cards1 and goal[i] == dq_cards1[0]:
            answer.append(dq_cards1.popleft())
        elif dq_cards2 and goal[i] == dq_cards2[0]:
            answer.append(dq_cards2.popleft())
        else:
            return "No"
    return "Yes"

print(solution(["i", "drink", "water"], ["want", "to"], ["i", "want", "to", "drink", "water"]))  # 출력 결과: Yes
print(solution(["i", "drink", "water"], ["want", "to"], ["want", "i", "to", "drink", "water"]))  # 출력 결과: No