## 문제 7 : 숫자 비교하기

# 기본 if문
def solution(num1, num2):
    if num1 == num2:
        answer = -1
    else:
        answer = 1
    return answer
print(solution(2, 3))
print(solution(11, 11))
print(solution(7, 99))


# 간결화
def solution(num1, num2): return -1 if num1 != num2 else 1
print(solution(2, 3))
print(solution(11, 11))
print(solution(7, 99))


# 람다 활용
solution = lambda num1, num2 : 1 if num1 == num2 else -1
print(solution(2, 3))
print(solution(11, 11))
print(solution(7, 99))


