## 문제 8 : 배열의 유사도

# for문 활용
def solution(s1, s2):
    answer = 0
    for i in s1:
        for j in s2:
            if i == j:
                answer += 1
    return answer

print(solution(["a", "b", "c"], ["com", "b", "d", "p", "c"]))
print(solution(["n", "omg"], ["m", "dot"]))

# 리스트 컴프리헨션
def solution(s1, s2): return 2 if [i for i in s1 for j in s2 if i == j] else 0

print(solution(["a", "b", "c"], ["com", "b", "d", "p", "c"]))
print(solution(["n", "omg"], ["m", "dot"]))

# set()함수 활용
def solution(s1, s2):
     return len(set(s1) & set(s2))
