from collections import deque
def solution(wallet, bill):
    answer = deque()
    answer.append((bill[0], bill[1], 0))
    while answer:
        w, h, folds = answer.popleft()
        if ((wallet[0] >=  w and wallet[1] >= h) or (wallet[0] >= h and wallet[1] >= w)):
            return folds
        if w > 1:
            answer.append((max(1, w//2), h, folds + 1))
        if h > 1:
            answer.append((w, max(1, h//2), folds + 1))
    return -1

print(solution([30, 15], [26, 17]))
print(solution([50, 50], [100, 241]))
