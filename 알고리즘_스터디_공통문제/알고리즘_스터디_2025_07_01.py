def solution(wallet, bill):
    answer = 0
    wmin, wmax = sorted(wallet)
    bw, bh = bill

    while True:
        bmin, bmax = sorted((bw, bh))
        if bmin <= wmin and bmax <= wmax:
            return answer
        if bw >= bh:
            bw //= 2
        else:
            bh //= 2
        answer += 1

print(solution([30, 15], [26, 17]))
print(solution([50, 50], [100, 241]))
