## 문제 36 : 배열의 길이에 따라 다른 연산하기

# 기본 for문. 시간복잡도 O(n)
def solution(arr, n):
    answer = []
    for i in range(len(arr)):
        if (len(arr) % 2 != 0) and (i % 2 == 0):
            answer.append(arr[i] + n)
        elif (len(arr) % 2 == 0) and (i % 2 != 0):
            answer.append(arr[i] + n)
        else:
            answer.append(arr[i])
    return answer

print(solution([49, 12, 100, 276, 33], 27))
print(solution([444, 555, 666, 777], 100))


# 간결화 + 변형 for문. 시간복잡도 O(n/2)
def solution(arr, n):
    if len(arr) % 2:
        for i in range(0, len(arr), 2):
            arr[i] += n
    else:
        for i in range(1, len(arr), 2):
            arr[i] += n
    return arr

print(solution([49, 12, 100, 276, 33], 27))
print(solution([444, 555, 666, 777], 100))

