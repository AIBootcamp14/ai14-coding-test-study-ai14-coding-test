## 문제 34 : 특정한 문자를 대문자로 바꾸기

def solution(my_string, alp):
    answer = []
    for i in my_string:
        if i in alp:
            answer.append(i.upper())
        else:
            answer.append(i)
    return ''.join(answer)

print(solution("programmers", "p"))
print(solution("lowercase", "x"))


